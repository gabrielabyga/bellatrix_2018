                     <p class="text-justify"> Uma galáxia é um grande sistema, gravitacionalmente ligado, que consiste de estrelas, remanescentes de estrelas, um meio interestelar de gás e poeira, e um importante mas insuficientemente conhecido componente apelidado de matéria escura. A palavra “galáxia” deriva do grego ‘’galaxias’’ (γαλαξίας), literalmente "leitoso", numa referência à nossa galáxia, a Via Láctea. Exemplos de galáxias variam desde as anãs, com até 10 milhões (107) de estrelas, até gigantes com cem trilhões (1014) de estrelas, todas orbitando o centro de massa da galáxia. <br> </br>
                    As galáxias contêm quantidades variadas de sistemas e aglomerados estelares e de tipos de nuvens interestelares. Entre esses objetos existe um meio interestelar esparso de gás, poeira e raios cósmicos. A matéria escura parece corresponder a cerca de 90% da massa da maioria das galáxias. Dados observacionais sugerem que podem existir buracos negros supermaciços no centro de muitas, se não todas as galáxias. Acredita-se que eles sejam o impulsionador principal dos núcleos galácticos ativos – região compacta no centro de algumas galáxias que tem uma luminosidade muito maior do que a comum. A Via Láctea parece possuir pelo menos um desses objetos. <br> </br>
                    As galáxias foram historicamente categorizadas segundo sua forma aparente, usualmente referida como sua morfologia visual. Uma forma comum é a galáxia elíptica, que tem um perfil de luminosidade em forma de elipse. Galáxias espirais têm forma de disco, com braços curvos. Aquelas com formas irregulares ou não usuais são conhecidas como galáxias irregulares e se originam tipicamente da disrupção pela atração gravitacional de galáxias vizinhas. Essas interações entre galáxias, que podem ao final resultar na sua junção, às vezes induzem o aumento significativo de incidentes de formação estelar, levando às galáxias starburst. Galáxias menores que não têm uma estrutura coerente são referidas como galáxias irregulares. <br> </br>
                    Existem provavelmente cerca de 2 trilhões de galáxias no universo observável, contendo mais estrelas do que grãos de areia no planeta Terra. Em sua maioria elas possuem de 1 000 a 100 000 parsecs de diâmetro e são separadas por distâncias da ordem de milhões de parsecs. O espaço intergaláctico é preenchido com um gás tênue com uma densidade média de menos de um átomo por metro cúbico. A maior parte das galáxias está organizada numa hierarquia de associações conhecidas como grupos e aglomerados, os quais, por sua vez, formam superaglomerados maiores. Numa escala maior, essas associações são geralmente organizadas em filamentos e muralhas, que são circundados por vazios imensos.


					 
                
