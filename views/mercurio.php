                     <p class="text-justify"> Mercúrio é o menor e mais interno planeta do Sistema Solar, orbitando o Sol a cada 87,969 dias terrestres. A sua órbita tem a maior excentricidade e o seu eixo apresenta a menor inclinação em relação ao plano da órbita dentre todos os planetas do Sistema Solar. Mercúrio completa três rotações em torno de seu eixo a cada duas órbitas. O periélio da órbita de Mercúrio apresenta uma precessão de 5600 segundos de arco por século, um fenômeno completamente explicado apenas a partir do século XX pela Teoria da Relatividade Geral formulada por Albert Einstein.A sua aparência é brilhante quando observado da Terra, tendo uma magnitude aparente que varia de −2,6 a 5,7, embora não seja facilmente observado pois sua separação angular do Sol é de apenas 28,3º. Uma vez que Mercúrio normalmente se perde no intenso brilho solar, exceto em eclipses solares, só pode ser observado a olho nu durante o crepúsculo matutino ou vespertino. <br> </br>

Comparado a outros planetas, pouco se sabe a respeito de Mercúrio, pois telescópios em solo terrestre revelam apenas um crescente iluminado com detalhes limitados. As duas primeiras espaçonaves a explorar o planeta foram a Mariner 10, que mapeou aproximadamente 45% da superfície do planeta entre 1974 e 1975, e a MESSENGER, que mapeou outros 30% da superfície durante um sobrevoo em 14 de janeiro de 2008. O último sobrevoo ocorreu em setembro de 2009 e a nave entrou em órbita do planeta em 18 de março de 2011, quando começou a mapear o restante do planeta, numa missão com duração nominal de um ano terrestre. <br> </br>

Mercúrio tem uma aparência similar à da Lua com crateras de impacto e planícies lisas, não possuindo satélites naturais nem uma atmosfera substancial. Entretanto, diferentemente da Lua, possui uma grande quantidade de ferro no núcleo que gera um campo magnético, cuja intensidade é cerca de 1% da intensidade do campo magnético da Terra. É um planeta excepcionalmente denso devido ao tamanho relativo de seu núcleo. A temperatura em sua superfície varia de 90 a 700 K (−183 °C a 427 °C). O ponto subsolar é a região mais quente e o fundo das crateras perto dos polos as regiões mais frias.

As primeiras observações registradas de Mercúrio datam pelo menos do primeiro milênio antes de Cristo. Antes do século IV a.C., astrônomos gregos acreditavam que se tratasse de dois objetos distintos: um visível no nascer do sol, ao qual chamavam Apolo, e outro visível ao pôr do Sol, chamado de Hermes.O nome em português para o planeta provém da Roma Antiga, onde o astro recebeu o nome do deus romano Mercúrio, que tinha na mitologia grega o nome de Hermes (Ἑρμῆς). O símbolo astronômico de Mercúrio é uma versão estilizada do caduceu de Hermes.


					 
                
